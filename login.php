    $(window).load(function() {

        // slider background
        $("#login-wrap,body").vegas({
            slides: [
                { src: "img/login1.jpg" },
                { src: "img/login2.jpg" },
                { src: "img/login3.jpg" },
                { src: "img/login4.jpg" }
            ]
        });


